#define LED_7_Pin GPIO_PIN_7
#define LED_7_GPIO_Port GPIOH
#define ISM330DHCX_INT1_Pin GPIO_PIN_11
#define ISM330DHCX_INT1_GPIO_Port GPIOE
#define ISM330DHCX_INT1_EXTI_IRQn EXTI11_IRQn

#define PUTCHAR_PROTOTYPE int __io_putchar(int ch)
#define GETCHAR_PROTOTYPE int __io_getchar(void)


ISM330DHCX_Object_t ism330dhcx_object;
ISM330DHCX_AxesRaw_t acc_values;

float x, y, z;

